def apply_wording(slug: str) -> None:
    cfg = PAGES[slug]
    cfg["leads"] = [soften_platform_wording(p) for p in cfg["leads"]]
    new_secs = []
    for h2, sid, paras in cfg["sections"]:
        nh2 = soften_platform_wording(h2)
        nsid = slug_id(nh2) if nh2 != h2 else sid
        new_secs.append((nh2, nsid, [soften_platform_wording(p) for p in paras]))
    cfg["sections"] = new_secs
    if "practical" in cfg:
        ph2 = soften_platform_wording(cfg["practical"]["h2"])
        cfg["practical"]["h2"] = ph2
        cfg["practical"]["intro"] = soften_platform_wording(cfg["practical"]["intro"])
        cfg["practical"]["tips"] = [soften_platform_wording(t) for t in cfg["practical"]["tips"]]
    cfg["faq"] = [(q, soften_platform_wording(a)) for q, a in cfg["faq"]]
    for key in ("cta_bottom_text", "cta_bottom_title"):
        if key in cfg and isinstance(cfg[key], str):
            cfg[key] = soften_platform_wording(cfg[key])


