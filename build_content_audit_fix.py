def plain(p: str) -> str:
    return re.sub(r"<[^>]+>", "", p).strip()


def clean_page(slug: str) -> None:
    cfg = PAGES[slug]
    new_secs = []
    for h2, sid, paras in cfg["sections"]:
        seen: set[str] = set()
        kept = []
        for p in paras:
            t = plain(p)
            if not t or t in seen:
                continue
            if any(m in t for m in BOILERPLATE_MARKERS):
                continue
            seen.add(t)
            kept.append(p)
        new_secs.append((h2, sid, kept))
    cfg["sections"] = new_secs


