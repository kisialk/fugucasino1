# Unique depth per page/section — no cross-page duplication


def add_depth(slug: str, extras: dict[str, list[str]]) -> None:
    new = []
    for h2, sid, paras in PAGES[slug]["sections"]:
        new.append((h2, sid, paras + extras.get(sid, [])))
    PAGES[slug]["sections"] = new


