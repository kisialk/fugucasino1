# Bulk paragraph injection for word-count targets (human-style expansions)


def bulk_extend(slug: str, per_section: list[str]) -> None:
    cfg = PAGES[slug]
    new = []
    for i, (h2, sid, paras) in enumerate(cfg["sections"]):
        extra = [per_section[i]] if i < len(per_section) else []
        new.append((h2, sid, paras + extra))
    cfg["sections"] = new


def long_faq(slug: str, items: list[tuple[str, str]]) -> None:
    PAGES[slug]["faq"] = PAGES[slug]["faq"] + items


