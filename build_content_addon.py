def extend_page(slug: str, addons: dict[str, list[str]]) -> None:
    cfg = PAGES[slug]
    new = []
    for h2, sid, paras in cfg["sections"]:
        new.append((h2, sid, paras + addons.get(sid, [])))
    cfg["sections"] = new
    extra_leads = addons.get("__leads__", [])
    if extra_leads:
        cfg["leads"] = cfg["leads"] + extra_leads


